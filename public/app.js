let currentSection = null;
let officialSamples = {};
let localSamples = {};
let generatedXml = '';

// ==================== INIT ====================
async function init() {
  await loadSectionList();  // loads sections dynamically from server
  const [gl, local] = await Promise.all([
    fetch('/api/samples').then(r=>r.json()).catch(()=>({sections:{}})),
    fetch('/api/local-samples').then(r=>r.json()).catch(()=>({sections:{}}))
  ]);
  officialSamples = gl.sections || {};
  localSamples = local.sections || {};
  renderSectionList();
  renderHeaderStats();
}

// ==================== SECTION LIST ====================
let activeSections = ['Handshake','FreeRooms','ActivityData','BaseRates','GuestRequests','Inventory','RatePlans'];

let sectionsMeta = {};  // name → {emoji, ctx, autoGenerated}

async function loadSectionList() {
  try {
    const r = await fetch('/api/sections').then(r => r.json());
    if (r.sections && r.sections.length > 0) activeSections = r.sections;
    if (r.withMeta) r.withMeta.forEach(m => { sectionsMeta[m.name] = m; });
  } catch {}
  const ul = document.getElementById('section-list');
  ul.innerHTML = activeSections.map(s => {
    const ai = sectionsMeta[s]?.autoGenerated ? ' <span title="Emoji von KI generiert" style="font-size:.65em;opacity:.5">🤖</span>' : '';
    return `<li onclick="selectSection('${s}')" id="nav-${s}">
      <span class="section-name">${sectionEmoji(s)} ${s}${ai}</span>
      <span class="counts">
        <span class="count-pill count-gl">…</span>
        <span class="count-pill count-local">…</span>
      </span>
    </li>`;
  }).join('');
  // Populate validation section filter dropdown
  const sel = document.getElementById('va-section-filter');
  if (sel) {
    sel.innerHTML = '<option value="">Alle Sections</option>';
    activeSections.forEach(s => {
      const opt = document.createElement('option');
      opt.value = s; opt.textContent = `${sectionEmoji(s)} ${s}`;
      sel.appendChild(opt);
    });
  }
}

function sectionEmoji(s) {
  // First: check dynamic metadata from server (covers auto-generated emojis too)
  if (sectionsMeta[s]?.emoji) return sectionsMeta[s].emoji;
  // Fallback: known hardcoded map (used before API loads)
  const map = {Handshake:'🤝',FreeRooms:'🛏️',ActivityData:'🎿',BaseRates:'💶',
               GuestRequests:'👤',Inventory:'🏨',RatePlans:'📋'};
  return map[s] || '📁';
}

function renderSectionList() {
  activeSections.forEach(s => {
    const li = document.getElementById(`nav-${s}`);
    if (!li) return;
    const glCount = (officialSamples[s]||[]).length;
    const localCount = (localSamples[s]||[]).length;
    li.querySelector('.count-gl').textContent = `🏛️${glCount}`;
    li.querySelector('.count-local').textContent = `📦${localCount}`;
  });
}

async function renderHeaderStats() {
  const glTotal = Object.values(officialSamples).reduce((s,a)=>s+a.length,0);
  const localTotal = Object.values(localSamples).reduce((s,a)=>s+a.length,0);

  // XSD versions status
  let xsdPill = '<span class="stat-pill" style="opacity:.6">⏳ Versionen laden...</span>';
  try {
    const vd = await fetch('/api/xsd-versions').then(r=>r.json());
    const versions = vd.versions || [];
    const outOfSync = versions.filter(v => v.remoteSynced === false);
    const checkedCount = versions.filter(v => v.remoteSynced !== null).length;
    let tooltip = versions.map(v => {
      const icon = v.remoteSynced === true ? '✅' : v.remoteSynced === false ? '⚠️' : '⏳';
      const dep = v.deprecated ? ' [veraltet]' : v.status==='latest' ? ' [neu]' : '';
      return `${icon} ${v.version}${dep}: ${v.hash||'–'}`;
    }).join('\n');
    if (outOfSync.length > 0) {
      xsdPill = `<span class="stat-pill" style="background:rgba(245,158,11,.15);color:#fbbf24;border-color:rgba(245,158,11,.4)" title="${tooltip}">⚠️ XSD ${outOfSync.map(v=>v.version).join(', ')} geändert!</span>`;
    } else if (checkedCount === versions.length && versions.length > 0) {
      xsdPill = `<span class="stat-pill green" title="${tooltip}">✅ ${versions.length} XSD-Versionen aktuell</span>`;
    } else {
      xsdPill = `<span class="stat-pill" style="opacity:.8" title="${tooltip}">🔍 ${versions.length} Spec-Versionen (${checkedCount} geprüft)</span>`;
    }
  } catch {}

  document.getElementById('header-stats').innerHTML = `
    <span class="stat-pill green">🏛️ ${glTotal} Official</span>
    <span class="stat-pill blue">📦 ${localTotal} Lokal</span>
    ${xsdPill}
    <span class="stat-pill">🤖 Claude AI</span>`;
}

// ==================== SELECT SECTION ====================
function selectSection(section) {
  currentSection = section;
  document.querySelectorAll('#section-list li').forEach(li => li.classList.remove('active'));
  document.getElementById(`nav-${section}`).classList.add('active');

  document.getElementById('welcome').style.display = 'none';
  document.getElementById('section-view').style.display = 'block';

  renderGapBanner(section);
  switchTab('official');
}

function renderGapBanner(section) {
  const gl = officialSamples[section] || [];
  const local = localSamples[section] || [];
  
  const countByType = (arr) => {
    const types = {};
    arr.forEach(f => {
      const t = detectType(f.name);
      types[t] = (types[t]||0) + 1;
    });
    return types;
  };
  
  const glTypes = countByType(gl);
  const localTypes = countByType(local);
  
  const typeLabels = {rq:'RQ',rs:'RS',neg:'NEG',inv:'INV',ack:'ACK',push:'PUSH',pull:'PULL',contrib:'Contrib'};
  
  let html = `
    <div class="gap-item">
      <span class="gap-icon">🏛️</span>
      <span class="gap-count gl">${gl.length}</span>
      <span class="gap-label">Offizielle Samples</span>
    </div>
    <span class="gap-sep">·</span>
    <div class="gap-item">
      <span class="gap-icon">📦</span>
      <span class="gap-count local">${local.length}</span>
      <span class="gap-label">Unsere XMLs</span>
    </div>`;
  
  // Type breakdown
  const allTypes = [...new Set([...Object.keys(glTypes),...Object.keys(localTypes)])];
  if (allTypes.length > 0) {
    html += '<span class="gap-sep">|</span>';
    allTypes.forEach(t => {
      const g = glTypes[t]||0, l = localTypes[t]||0;
      html += `<div class="gap-item"><span class="gap-label">${typeLabels[t]||t}:</span><span class="gap-count ${g===0?'zero':'gl'}" title="Offiziell">${g}</span><span class="gap-label">/</span><span class="gap-count local" title="Lokal">${l}</span></div>`;
    });
  }
  
  document.getElementById('gap-banner').innerHTML = html;
}

// ==================== TYPE DETECTION ====================
function detectType(name) {
  const n = name.toLowerCase();
  if (n.includes('-neg-')||n.startsWith('neg-')) return 'neg';
  if (n.includes('-inv-')||n.startsWith('inv-')) return 'inv';
  if (n.includes('ack-')) return 'ack';
  if (n.includes('push-')) return 'push';
  if (n.includes('pull-')) return 'pull';
  if (n.includes('contrib/')) return 'contrib';
  if (n.includes('nonvalid/')) return 'neg';
  if (n.includes('_rq')||n.includes('-rq')) return 'rq';
  if (n.includes('_rs')||n.includes('-rs')) return 'rs';
  return 'rq';
}

function typeBadge(type) {
  const labels = {rq:'RQ',rs:'RS',neg:'NEG',inv:'INV',ack:'ACK',push:'PUSH',pull:'PULL',contrib:'contrib'};
  return `<span class="card-badge badge-${type}">${labels[type]||type.toUpperCase()}</span>`;
}

// ==================== TABS ====================
function switchTab(tab) {
  document.querySelectorAll('.tab').forEach(t => t.classList.toggle('active', t.dataset.tab===tab));
  document.querySelectorAll('.tab-content').forEach(t => t.classList.remove('active'));
  document.getElementById(`tab-${tab}`).classList.add('active');

  if (tab==='official') renderOfficialGrid();
  if (tab==='local') renderLocalGrid();
  if (tab==='compare') renderCompareGrid();
  if (tab==='validate-all') { /* rendered on demand */ }
}


// ==================== COMPARE GRID ====================
function renderCompareGrid() {
  const grid = document.getElementById('compare-grid');
  if (!currentSection) { grid.innerHTML = '<div class="card-empty">Bitte zuerst eine Section auswählen</div>'; return; }

  const glItems  = officialSamples[currentSection] || [];
  const locItems = localSamples[currentSection]    || [];

  // Group by type
  const typeOrder = ['rq','rs','push','pull','ack','neg','inv','contrib'];
  const typeLabels = {rq:'📤 RQ – Requests',rs:'📥 RS – Responses',push:'📡 PUSH',pull:'🔽 PULL',ack:'✅ ACK',neg:'❌ NEG – Ungültige',inv:'🔍 INV – Randfall',contrib:'🤝 Contrib'};

  function groupByType(arr) {
    const g = {};
    arr.forEach(f => {
      const t = detectType(f.name);
      (g[t] = g[t]||[]).push(f);
    });
    return g;
  }

  const glByType  = groupByType(glItems);
  const locByType = groupByType(locItems);
  const allTypes  = [...new Set([...Object.keys(glByType), ...Object.keys(locByType)])]
    .sort((a,b) => typeOrder.indexOf(a) - typeOrder.indexOf(b));

  if (allTypes.length === 0) {
    grid.innerHTML = '<div class="card-empty">Keine Samples für diese Section vorhanden</div>';
    return;
  }

  function itemCard(f, source) {
    const badge = typeBadge(detectType(f.name));
    const shortName = f.name.replace(/\.(xml)$/i,'').replace(/^.*\//,'');
    const apiPath = source==='official'
      ? '/api/sample-content?path='+encodeURIComponent(f.path)
      : '/api/local-content?file='+encodeURIComponent(f.path||(f.name));
    return `<div class="compare-item" onclick="openCompareViewer('${apiPath}','${shortName.replace(/'/g,'\'')}')">
      <span class="ci-name">${shortName}</span>${badge}
    </div>`;
  }

  let html = '';
  allTypes.forEach(type => {
    const glArr  = glByType[type]  || [];
    const locArr = locByType[type] || [];
    const label  = typeLabels[type] || type.toUpperCase();
    const diff   = locArr.length - glArr.length;
    const diffBadge = diff > 0
      ? `<span style="color:#34d399;font-size:0.78em;margin-left:8px">+${diff} mehr lokal ✨</span>`
      : diff < 0
      ? `<span style="color:#f87171;font-size:0.78em;margin-left:8px">${diff} weniger</span>`
      : `<span style="color:var(--text-muted);font-size:0.78em;margin-left:8px">gleich</span>`;

    html += `<div class="compare-section">
      <div class="compare-type-header">${label} &nbsp;—&nbsp; 🏛️ ${glArr.length} offiziell / 📦 ${locArr.length} lokal ${diffBadge}</div>
      <div class="compare-cols">
        <div>
          <div class="compare-col-header official">🏛️ Offizielle Samples</div>
          ${glArr.length ? glArr.map(f=>itemCard(f,'official')).join('') : '<div class="compare-col-empty">Keine offiziellen Samples</div>'}
        </div>
        <div>
          <div class="compare-col-header local">📦 Unsere XMLs</div>
          ${locArr.length ? locArr.map(f=>itemCard(f,'local')).join('') : '<div class="compare-col-empty">Noch keine lokalen Samples<br><span style=\'cursor:pointer;color:var(--accent)\' onclick=\'switchTab(\"generate\")\'> → KI generieren</span></div>'}
        </div>
      </div>
    </div>`;
  });

  grid.innerHTML = html;
}

function openCompareViewer(apiPath, title) {
  const type = detectType(title);
  openModal(title, type, fetch(apiPath).then(r => r.text()));
}


// ==================== VALIDATION BATCH RUN ====================
let lastValidationReport = null;

async function runValidationBatch() {
  const btn = document.getElementById('va-run-btn');
  const btnText = document.getElementById('va-run-text');
  const resultsDiv = document.getElementById('va-results');
  const specVersion = document.getElementById('va-spec-version').value;
  const section = document.getElementById('va-section-filter').value || null;

  btn.disabled = true;
  btnText.innerHTML = '<span class="spinner"></span>Läuft...';
  document.getElementById('va-export-btn').style.display = 'none';
  resultsDiv.innerHTML = `
    <div style="color:var(--text-muted);font-size:.85em;margin-bottom:8px">
      Validiere gegen AlpineBits ${specVersion}${section?' ('+section+'):':', alle Sections:'} bitte warten...
    </div>
    <div class="va-progress"><div class="va-progress-bar" style="width:30%"></div></div>`;

  try {
    const r = await fetch('/api/validate-all', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ specVersion, section })
    }).then(r => r.json());

    if (r.error) throw new Error(r.error);
    lastValidationReport = r;
    renderValidationResults(r);
    document.getElementById('va-export-btn').style.display = '';
  } catch(e) {
    resultsDiv.innerHTML = `<div class="validation-err">❌ Fehler: ${e.message}</div>`;
  } finally {
    btn.disabled = false;
    btnText.textContent = '▶ Erneut ausführen';
  }
}

function renderValidationResults(r) {
  const { summary, results, specVersion, xsdHash } = r;
  const scoreColor = summary.scorePercent >= 90 ? 'green' : summary.scorePercent >= 70 ? 'yellow' : 'red';
  const invTotal = results.filter(x => x.isInv).length;
  const validTotal = results.filter(x => !x.isInv).length;

  let html = `
    <div style="font-size:.78em;color:var(--text-muted);margin-bottom:12px">
      📋 AlpineBits ${specVersion} · XSD ${xsdHash} · ${new Date(r.timestamp).toLocaleString('de-AT')}
    </div>
    <div class="va-score-card">
      <div class="va-score-item">
        <div class="va-score-num ${scoreColor}">${summary.scorePercent}%</div>
        <div class="va-score-label">Gesamt-Score</div>
      </div>
      <div class="va-score-item">
        <div class="va-score-num green">${summary.correct}</div>
        <div class="va-score-label">Korrekt</div>
      </div>
      <div class="va-score-item">
        <div class="va-score-num red">${summary.incorrect}</div>
        <div class="va-score-label">Fehler</div>
      </div>
      <div class="va-score-item">
        <div class="va-score-num blue">${validTotal}</div>
        <div class="va-score-label">RQ/RS/NEG</div>
      </div>
      <div class="va-score-item">
        <div class="va-score-num yellow">${invTotal}</div>
        <div class="va-score-label">INV-Tests</div>
      </div>
    </div>`;

  // Legend
  html += `<div style="font-size:.76em;color:var(--text-muted);margin-bottom:16px;padding:8px 12px;background:var(--bg-secondary);border-radius:6px;border:1px solid var(--border)">
    ✅ = korrekt &nbsp;|&nbsp; ❌ = Fehler &nbsp;|&nbsp;
    🟢 RQ/RS/NEG sollte <strong>gültig</strong> sein &nbsp;|&nbsp;
    🔴 INV sollte <strong>ungültig</strong> sein (XSD-Fehler = erwartetes Verhalten!)
  </div>`;

  // Group by section
  const sections = [...new Set(results.map(x => x.section))];
  for (const sec of sections) {
    const secResults = results.filter(x => x.section === sec);
    const secCorrect = secResults.filter(x => x.correct).length;
    const secPct = Math.round(secCorrect / secResults.length * 100);
    const secColor = secPct === 100 ? '#34d399' : secPct >= 70 ? '#fbbf24' : '#f87171';

    html += `<div class="va-section-block">
      <div class="va-section-title">
        <span>${sectionEmoji ? sectionEmoji(sec) : ''} ${sec}</span>
        <span class="va-section-score" style="color:${secColor}">${secCorrect}/${secResults.length} (${secPct}%)</span>
      </div>`;

    for (const item of secResults) {
      const statusIcon = item.correct
        ? (item.isInv ? '✅' : '✅')
        : '❌';
      const typeIcon = item.isInv ? '🔴' : '🟢';
      const rowClass = item.correct ? 'correct' : 'incorrect';
      const shortName = item.filename.replace(/\.xml$/i, '');
      const detail = item.isInv
        ? (item.correct ? 'INV korrekt abgelehnt' : 'INV sollte ungültig sein!')
        : (item.correct ? 'XSD-konform' : 'XSD-Fehler!');

      html += `<div class="va-file-row ${rowClass}">
        <span class="va-file-status">${statusIcon}</span>
        <span class="va-file-type">${typeIcon}</span>
        <span class="va-file-name">${shortName}</span>
        <span style="font-size:.72em;color:var(--text-muted)">${detail}</span>
      </div>`;

      if (!item.correct && item.error) {
        html += `<div class="va-file-error">↳ ${item.error}</div>`;
      }
    }
    html += '</div>';
  }

  document.getElementById('va-results').innerHTML = html;
}

function exportValidationReport() {
  if (!lastValidationReport) return;
  const r = lastValidationReport;
  const lines = [
    `AlpineBits Validierungsbericht`,
    `==============================`,
    `Spec-Version: ${r.specVersion}`,
    `XSD-Hash:     ${r.xsdHash}`,
    `Datum:        ${new Date(r.timestamp).toLocaleString('de-AT')}`,
    ``,
    `ZUSAMMENFASSUNG`,
    `---------------`,
    `Gesamt:   ${r.summary.total}`,
    `Korrekt:  ${r.summary.correct} (${r.summary.scorePercent}%)`,
    `Fehler:   ${r.summary.incorrect}`,
    ``,
    `DETAILS`,
    `-------`,
  ];
  for (const item of r.results) {
    const status = item.correct ? 'OK' : 'FAIL';
    const expected = item.isInv ? 'INVALID' : 'VALID';
    lines.push(`[${status}] ${item.section}/${item.filename} (erwartet: ${expected})`);
    if (!item.correct && item.error) lines.push(`       Fehler: ${item.error}`);
  }
  const blob = new Blob([lines.join('\n')], {type:'text/plain'});
  const a = document.createElement('a');
  a.href = URL.createObjectURL(blob);
  a.download = `alpinebits-validation-${r.specVersion}-${new Date(r.timestamp).toISOString().slice(0,10)}.txt`;
  a.click();
}

