// ==================== GENERATE ====================
async function generateXml() {
  if (!currentSection) { alert('Bitte zuerst eine Section wählen'); return; }
  
  const type = document.getElementById('gen-type').value;
  const desc = document.getElementById('gen-desc').value;
  const btn = document.getElementById('gen-btn');
  const btnText = document.getElementById('gen-btn-text');
  
  btn.disabled = true;
  btnText.innerHTML = '<span class="spinner"></span>Claude generiert...';
  document.getElementById('gen-result').style.display = 'none';
  
  // Get a few examples for context
  const examples = [];
  const glSamples = officialSamples[currentSection] || [];
  for (const s of glSamples.slice(0,2)) {
    try {
      const content = await fetch(`/api/sample-content?path=${encodeURIComponent(s.path)}`).then(r=>r.text());
      examples.push({ name: s.name, content: content.slice(0,1500) });
    } catch {}
  }
  
  try {
    const apiKey = (document.getElementById('gen-apikey')?.value || '').trim();
    const r = await fetch('/api/generate',{
      method:'POST',
      headers:{'Content-Type':'application/json'},
      body:JSON.stringify({ section:currentSection, type, description:desc, examples, ...(apiKey ? { apiKey } : {}) })
    }).then(r=>r.json());
    
    if (r.error) throw new Error(r.error);
    
    generatedXml = r.xml;
    const filename = `${currentSection}-${type.replace(/\s+/g,'-')}.xml`;
    
    document.getElementById('gen-filename').textContent = filename;
    document.getElementById('gen-code').textContent = generatedXml;
    document.getElementById('validation-result').innerHTML = '';
    document.getElementById('gen-result').style.display = 'block';
    hljs.highlightElement(document.getElementById('gen-code'));
    
  } catch(e) {
    alert('Fehler: ' + e.message);
  }
  
  btn.disabled = false;
  btnText.textContent = '🤖 Mit Claude generieren';
}

async function validateGenerated() {
  if (!generatedXml) return;
  const specVersion = document.getElementById('gen-spec-version')?.value || '2022-10';
  const div = document.getElementById('validation-result');
  div.innerHTML = '<div class="validation-ok">⏳ Validiere gegen AlpineBits ' + specVersion + '...</div>';
  const r = await fetch('/api/validate',{method:'POST',headers:{'Content-Type':'application/json'},
    body:JSON.stringify({xml:generatedXml, specVersion})}).then(r=>r.json());
  const versionTag = `<span style="font-size:.75em;opacity:.7;margin-left:6px">AlpineBits ${r.specVersion||specVersion}${r.xsdHash?' · '+r.xsdHash:''}</span>`;
  div.innerHTML = r.valid
    ? `<div class="validation-ok">✅ Valid — XSD-konform!${versionTag}</div>`
    : `<div class="validation-err">❌ Validation Error:${versionTag}\n${r.error}</div>`;
}

