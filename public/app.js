// ==================== CREDENTIALS ====================
function loadCredentials() {
  const token = localStorage.getItem('gl_token');
  const username = localStorage.getItem('gl_username');
  if (token && username) {
    document.getElementById('gl-token').value = token;
    document.getElementById('gl-username').value = username;
    updateCredsStatus(true, username);
  }
}

function updateCredsStatus(connected, username='') {
  const btn = document.getElementById('creds-status-btn');
  if (connected) {
    btn.textContent = `✅ @${username}`;
    btn.classList.add('connected');
    const mrBtn = document.getElementById('btn-mr');
    if (mrBtn) mrBtn.disabled = false;
  } else {
    btn.textContent = '🔑 GitLab verbinden';
    btn.classList.remove('connected');
  }
}

function openCreds() {
  loadCredentials();
  document.getElementById('creds-modal').classList.add('open');
}
function closeCreds() {
  document.getElementById('creds-modal').classList.remove('open');
}

async function testAndSaveCreds() {
  const token = document.getElementById('gl-token').value.trim();
  const username = document.getElementById('gl-username').value.trim();
  if (!token || !username) { alert('Bitte Token und Username eingeben'); return; }
  
  const btn = document.getElementById('creds-btn-text');
  const result = document.getElementById('creds-test-result');
  btn.innerHTML = '<span class="spinner"></span>Teste Verbindung...';
  result.innerHTML = '';
  
  try {
    const r = await fetch(`https://gitlab.com/api/v4/users?username=${username}`, {
      headers: { 'Authorization': `Bearer ${token}`, 'User-Agent': 'AlpineBits-Manager/1.0' }
    });
    if (!r.ok) throw new Error(`HTTP ${r.status} — Token ungültig?`);
    const users = await r.json();
    if (!users.length) throw new Error('Username nicht gefunden');
    
    localStorage.setItem('gl_token', token);
    localStorage.setItem('gl_username', username);
    updateCredsStatus(true, username);
    
    result.innerHTML = `<div class="validation-ok">✅ Verbunden als @${users[0].name} (${username})</div>`;
    setTimeout(closeCreds, 1500);
  } catch(e) {
    result.innerHTML = `<div class="validation-err">❌ ${e.message}</div>`;
  }
  btn.textContent = '✅ Speichern & Testen';
}

function clearCreds() {
  localStorage.removeItem('gl_token');
  localStorage.removeItem('gl_username');
  document.getElementById('gl-token').value = '';
  document.getElementById('gl-username').value = '';
  document.getElementById('creds-test-result').innerHTML = '';
  updateCredsStatus(false);
}

