async function submitMR() {
  const token = localStorage.getItem('gl_token');
  const username = localStorage.getItem('gl_username');
  const filename = document.getElementById('mr-filename').value.trim();
  const desc = document.getElementById('mr-desc').value.trim();
  const btn = document.getElementById('mr-submit-text');
  
  if (!filename) { alert('Bitte Dateiname eingeben'); return; }
  
  btn.innerHTML = '<span class="spinner"></span>Fork → Branch → Upload → MR...';
  document.querySelector('#mr-modal .btn-generate').disabled = true;
  
  try {
    const r = await fetch('/api/gitlab/create-mr', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ token, username, section: currentSection, filename, xml: generatedXml, description: desc })
    }).then(r => r.json());
    
    if (r.error) throw new Error(r.error);
    
    document.getElementById('mr-modal-body').innerHTML = `
      <div class="mr-success">
        <div class="mr-success-icon">🎉</div>
        <h3>Merge Request erstellt!</h3>
        <p>Das XML wurde in deinen Fork hochgeladen und ein MR zum offiziellen AlpineBits Repository erstellt.</p>
        <a href="${r.mrUrl}" target="_blank">🔗 MR #${r.mrIid} auf GitLab öffnen ↗</a>
        <div class="mr-details">
          📁 Datei: <code>${r.filePath}</code><br>
          🌿 Branch: <code>${r.branch}</code>
        </div>
      </div>`;
  } catch(e) {
    document.getElementById('mr-modal-body').innerHTML += `
      <div class="validation-err" style="margin-top:12px">❌ ${e.message}</div>`;
    document.querySelector('#mr-modal .btn-generate').disabled = false;
    btn.textContent = '📤 Merge Request erstellen';
  }
}

